# --------------------------------------------------------------------------
# Household-disjoint holdout (side A), matching the floor's committed rule.
# --------------------------------------------------------------------------
def holdout_side_a_households(
    household_ids_universe: np.ndarray, seed: int, fraction: float = 0.5
) -> np.ndarray:
    """Side-A (holdout) household ids for a gate seed.

    Mirrors ``harness.panel.split_panel_by_person`` on ``household_id``: the
    sorted unique household universe, ``default_rng(seed).random(n) <
    fraction`` picks side A. Identical to the floor's committed rule.
    """
    ids = np.sort(np.unique(household_ids_universe))
    rng = np.random.default_rng(seed)
    picked = rng.random(len(ids)) < fraction
    return ids[picked]


# --------------------------------------------------------------------------
# Family-A regenerators (each returns a per-person array on the given slice).
# --------------------------------------------------------------------------
def regenerate_earnings(
    ages: np.ndarray,
    rng: np.random.Generator,
    marginals: dict,
    age_bin_fn: Callable[[np.ndarray], np.ndarray],
    period: int = TERMINAL_PERIOD,
) -> np.ndarray:
    """Draw fresh earnings from the certified gate-1 cell marginals.

    For each person: ``u ~ U(0,1)``; zero if ``u < p0`` of the cell,
    else ``quantile((u - p0) / (1 - p0))``. Vectorised per age bin.
    """
    ages = np.asarray(ages, dtype=np.float64)
    bins = age_bin_fn(ages)
    out = np.zeros(len(ages), dtype=np.float64)
    u = rng.random(len(ages))
    for b in np.unique(bins):
        cell = marginals.get((int(b), int(period)))
        if cell is None:
            continue
        idx = np.nonzero(bins == b)[0]
        ub = u[idx]
        pos = ub >= cell.p0
        if cell.p0 < 1.0 and pos.any():
            pr = (ub[pos] - cell.p0) / (1.0 - cell.p0)
            out[idx[pos]] = cell.quantile(pr)
    return out


def _synthetic_marital_panel(
    person_id: np.ndarray,
    age: np.ndarray,
    is_female: np.ndarray,
    weight: np.ndarray,
):
    from populace_dynamics.data import transitions

    birth_year = (REF_YEAR - age).astype(int)
    attrs = pd.DataFrame(
        {
            "person_id": person_id,
            "birth_year": birth_year,
            "sex": np.where(is_female, "female", "male"),
            "start_exposure_year": birth_year + 18,
            "censor_year": REF_YEAR,
            "weight": weight,
        }
    )
    empty = pd.DataFrame(
        {
            "person_id": pd.array([], dtype="int64"),
            "year": pd.array([], dtype="int64"),
            "marital_state": pd.array([], dtype="object"),
            "marriage_duration": pd.array([], dtype="Int64"),
            "years_since_dissolution": pd.array([], dtype="Int64"),
        }
    )
    return transitions.MaritalPanel(
        person_years=empty, events=pd.DataFrame(), attrs=attrs
    )


def regenerate_marital(
    person_id: np.ndarray,
    age: np.ndarray,
    is_female: np.ndarray,
    weight: np.ndarray,
    fitted_ft: Any,
    seed: int,
) -> pd.Series:
    """Terminal marital status per person via CANDIDATE_16 (ft.simulate)."""
    from populace_dynamics.models import family_transitions as ft

    panel = _synthetic_marital_panel(person_id, age, is_female, weight)
    hold = set(int(x) for x in person_id)
    sim_panel, _ = ft.simulate(panel, hold, fitted_ft, seed)
    py = sim_panel.person_years
    term = py.loc[
        py["year"] == py.groupby("person_id")["year"].transform("max")
    ]
    return term.set_index("person_id")["marital_state"]


def _synthetic_household_panels(
    person_id: np.ndarray,
    age: np.ndarray,
    is_female: np.ndarray,
    weight: np.ndarray,
):
    """Multi-wave household panel + matching marital panel for CPS persons."""
    from populace_dynamics.data import household_composition as hcd
    from populace_dynamics.data import transitions

    sexes = np.where(is_female, "female", "male")
    birth_year = (REF_YEAR - age).astype(int)
    n = len(person_id)

    # Vectorised multi-wave expansion: waves every HH_CADENCE years from the
    # person's HH_ENTRY_AGE (>= START_AGE) to REF_YEAR inclusive.
    frames = []
    ages_i = age.astype(int)
    for i in range(n):
        a0 = ages_i[i]
        entry_age = max(HH_ENTRY_AGE, hcd.START_AGE)
        first_year = REF_YEAR - (a0 - entry_age)
        years = np.arange(first_year, REF_YEAR + 1, HH_CADENCE)
        if len(years) == 0 or years[-1] != REF_YEAR:
            years = np.append(years, REF_YEAR)
        wave_ages = a0 - (REF_YEAR - years)
        keep = wave_ages >= hcd.START_AGE
        years, wave_ages = years[keep], wave_ages[keep]
        if len(years) == 0:
            years, wave_ages = np.array([REF_YEAR]), np.array([a0])
        frames.append(
            pd.DataFrame(
                {
                    "person_id": person_id[i],
                    "year": years.astype(int),
                    "age": wave_ages.astype(int),
                    "sex": sexes[i],
                    "weight": weight[i],
                }
            )
        )
    pw = pd.concat(frames, ignore_index=True)
    pw["band"] = pw["age"].map(hcd._band_of)
    for flag in hcd.CORESIDENCE_LINKS:
        pw[flag] = False
    pw["multigen"] = False
    pw["hh_size"] = 1
    pw = hcd._add_transitions(pw)
    attrs = pw[["person_id"]].drop_duplicates().reset_index(drop=True)
    hh_panel = hcd.HouseholdCompositionPanel(person_waves=pw, attrs=attrs)

    mattrs = pd.DataFrame(
        {
            "person_id": person_id,
            "birth_year": birth_year,
            "sex": sexes,
            "start_exposure_year": birth_year + 18,
            "censor_year": REF_YEAR,
            "weight": weight,
        }
    )
    empty = pd.DataFrame(
        {
            "person_id": pd.array([], dtype="int64"),
            "year": pd.array([], dtype="int64"),
            "marital_state": pd.array([], dtype="object"),
            "marriage_duration": pd.array([], dtype="Int64"),
            "years_since_dissolution": pd.array([], dtype="Int64"),
        }
    )
    mpanel = transitions.MaritalPanel(
        person_years=empty, events=pd.DataFrame(), attrs=mattrs
    )
    return hh_panel, mpanel


def regenerate_household_size(
    person_id: np.ndarray,
    age: np.ndarray,
    is_female: np.ndarray,
    weight: np.ndarray,
    fitted_hc: Any,
    seed: int,
) -> pd.Series:
    """Terminal household size per person via CANDIDATE_9 (hc.simulate)."""
    from populace_dynamics.models import household_composition as hc

    hh_panel, mpanel = _synthetic_household_panels(
        person_id, age, is_female, weight
    )
    hold = set(int(x) for x in person_id)
    out = hc.simulate(hh_panel, mpanel, fitted_hc, hold, seed)
    pwo = out.person_waves
    term = pwo.loc[
        pwo["year"] == pwo.groupby("person_id")["year"].transform("max")
    ]
    return term.set_index("person_id")["hh_size"]


#: The generators certify ADULT dynamics (family_transitions START_AGE 15;
#: earnings ages 25-59; ADULT_BANDS 25+). The regenerated family-A surface is
#: the adult universe (age >= 18) the deployed generators cover. This matches
#: the floor's implicit scope for earnings (EARN_BANDS 18+), marital /
#: coresident (ADULT_BANDS 25+) exactly; ONLY hh_size_share differs -- the
#: committed rate_a is all-person while the household generator composes only
#: adults, a disclosed scope caveat (children are outside its universe).
ADULT_MIN_AGE = 18


def regenerate_person_frame(
    slice_df: pd.DataFrame, gens: DeployedGenerators, k: int, stream_base: int
) -> pd.DataFrame:
    """Regenerate the scored family-A surface for one draw k on a slice.

    Returns a person frame with REGENERATED ``earnings`` / ``marital_status``
    / ``hh_size`` / ``coresident_spouse`` and the permitted covariates
    (``age`` / ``is_female`` / ``weight``), ready for
    :func:`deployment_frame.reference_moments`. Restricted to the adult
    universe (:data:`ADULT_MIN_AGE`) the generators cover.
    """
    seed = stream_base + k
    rng = np.random.default_rng(seed)
    adults = slice_df[slice_df["age"] >= ADULT_MIN_AGE].reset_index(drop=True)
    pid = adults["person_id"].to_numpy()
    age = adults["age"].to_numpy(dtype=np.float64)
    fem = adults["is_female"].to_numpy(dtype=bool)
    wt = adults["weight"].to_numpy(dtype=np.float64)

    earn = regenerate_earnings(
        age, rng, gens.earnings_marginals, gens.age_bin_fn
    )
    marital = regenerate_marital(pid, age, fem, wt, gens.fitted_ft, seed)
    hh_size = regenerate_household_size(
        pid, age, fem, wt, gens.fitted_hc, seed
    )
    marital_arr = marital.reindex(pid).to_numpy(dtype=object)
    hh_arr = hh_size.reindex(pid).to_numpy()
    coresident = marital_arr == "married"

    return pd.DataFrame(
        {
            "person_id": pid,
            "weight": wt,
            "age": age,
            "is_female": fem,
            "earnings": earn,
            "marital_status": marital_arr,
            "hh_size": hh_arr,
            "coresident_spouse": coresident,
        }
    )


