def fit_generators(m4_artifact_path: str) -> DeployedGenerators:
    """Fit all certified generators once on the full PSID sample, frozen."""
    from populace_dynamics.data import disability
    from populace_dynamics.models import family_transitions as ft
    from populace_dynamics.models import household_composition as hc
    from populace_dynamics.models.family_transitions import (
        evaluation as fteval,
    )

    prov: dict[str, Any] = {}

    # gate-1 earnings marginals ---------------------------------------------
    t = time.time()
    g1, base = _import_gate1()
    panel = base.load_filtered_panel()
    marginals = g1.fit_cell_marginals(panel)
    prov["gate1"] = {
        "module": "run_gate1_candidate5b.fit_cell_marginals",
        "panel_rows": int(len(panel)),
        "n_cells": len(marginals),
        "age_min": int(base.AGE_MIN),
        "age_max": int(base.AGE_MAX),
        "period_min": int(base.PERIOD_MIN),
        "period_max": int(base.PERIOD_MAX),
        "fit_seconds": round(time.time() - t, 1),
    }

    # family_transitions CANDIDATE_16 (2a/2b marital) -----------------------
    t = time.time()
    src = fteval._load_sources()
    ft_ids = frozenset(src.panel.attrs["person_id"].tolist())
    ctx = ft.FitContext(
        panel=src.panel,
        demographic_panel=src.demographic_panel,
        marriage_records=src.marriage_records,
        birth_records=src.birth_records,
        marriage_order_map=src.order_map,
        train_ids=ft_ids,
    )
    fitted_ft = ft.REGISTRY.fit(ft.CANDIDATE_16, ctx)
    prov["family_transitions"] = {
        "candidate_id": ft.CANDIDATE_16.candidate_id,
        "sha256": ft.CANDIDATE_16.sha256,
        "n_train_persons": len(ft_ids),
        "fit_seconds": round(time.time() - t, 1),
    }

    # household_composition CANDIDATE_9 (2b household) ----------------------
    t = time.time()
    hc_sources = hc.load_sources()
    hc_ids = frozenset(hc_sources["hh"].attrs["person_id"].tolist())
    hc_ctx = hc.fit_context_from_sources(hc_sources, hc_ids)
    fitted_hc = hc.REGISTRY.fit(hc.CANDIDATE_9, hc_ctx)
    prov["household_composition"] = {
        "candidate_id": hc.CANDIDATE_9.candidate_id,
        "sha256": hc.CANDIDATE_9.sha256,
        "n_train_persons": len(hc_ids),
        "fit_seconds": round(time.time() - t, 1),
    }

    # gate_m4 disability prevalence cells (locked machinery) -----------------
    import json

    m4 = json.load(open(m4_artifact_path))
    m4_prev: dict[tuple[str, int, int, str], float] = {}
    for lo, hi in disability.DI_AGE_BANDS:
        band = f"{lo}-{hi}"
        for sex in disability.SEXES:
            key = f"prevalence.{band}|{sex}"
            rate = m4["reference_moments"][key]["rate"]
            m4_prev[(band, lo, hi, sex)] = float(rate)
    prov["gate_m4"] = {
        "artifact": m4_artifact_path.split("/")[-1],
        "run": m4.get("run"),
        "bands": [f"{lo}-{hi}" for lo, hi in disability.DI_AGE_BANDS],
        "source": "runs/m4_disability_v1.json reference_moments prevalence",
    }

    return DeployedGenerators(
        earnings_marginals=marginals,
        age_bin_fn=g1.age_bin,
        fitted_ft=fitted_ft,
        fitted_hc=fitted_hc,
        m4_prevalence=m4_prev,
        m4_bands=disability.DI_AGE_BANDS,
        fit_provenance=prov,
    )


