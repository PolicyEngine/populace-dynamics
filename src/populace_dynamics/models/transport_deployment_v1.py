# Family B -- SSA administrative anchors (ANCHOR-priced), M4-simulated.
# --------------------------------------------------------------------------
#: The conditioning columns family B's generators read (enumerated for the
#: candidate_conditioning_columns_rule); a zeroing frame must fail loudly.
FAMILY_B_CONDITIONING_COLUMNS = ("age", "is_female")
FRA_AGE = 66  # DI-worker stock cap (60-fra band top); conversions happen ~FRA


def _m4_band_of(age: float, bands: tuple[tuple[int, int], ...]):
    """M4 fitted band for an age; None below the youngest band (no DI for
    children -- disabled-worker eligibility starts in the 20-29 band) and
    None above the DI-worker cap (60-fra; older workers have converted)."""
    a = int(age)
    lo0, _ = bands[0]
    if a < lo0 or a > FRA_AGE:
        return None
    for lo, hi in bands:
        if lo <= a <= hi:
            return (lo, hi)
    return bands[-1]


def simulate_di_status(
    persons: pd.DataFrame, gens: DeployedGenerators, rng: np.random.Generator
) -> np.ndarray:
    """DI status per person from the deployed M4 prevalence (Bernoulli).

    ``on_di[i] ~ Bernoulli(m4_prevalence[m4_band(age_i), sex_i])`` for ages
    up to the DI-worker cap; False above it. Conditions ONLY on age + sex --
    never the frame's DI column or any SS proxy.
    """
    age = persons["age"].to_numpy(dtype=np.float64)
    fem = persons["is_female"].to_numpy(dtype=bool)
    prob = np.zeros(len(persons), dtype=np.float64)
    for i in range(len(persons)):
        band = _m4_band_of(age[i], gens.m4_bands)
        if band is None:
            continue
        sex = "female" if fem[i] else "male"
        prob[i] = gens.m4_prevalence[
            (f"{band[0]}-{band[1]}", band[0], band[1], sex)
        ]
    return rng.random(len(persons)) < prob


def _di_anchor_band(age: float) -> str | None:
    a = int(age)
    for label, lo, hi in DI_ANCHOR_BANDS:
        if lo <= a <= hi:
            return label
    return None


def family_b_score(
    persons: pd.DataFrame,
    gens: DeployedGenerators,
    family_b_contract: dict[str, Any],
    *,
    k_draws: int = K_DRAWS,
    pe_us_dir: str | None = None,
) -> dict[str, Any]:
    """Score family B on the FULL frame: di_prevalence + conversion, K=20."""
    from populace_dynamics import disability_conversion as dconv
    from populace_dynamics.ss import params as ssparams

    # assert conditioning columns populated (a zeroing frame fails loudly)
    populated = {}
    for col in FAMILY_B_CONDITIONING_COLUMNS:
        frac = float((persons[col].fillna(0).to_numpy() != 0).mean())
        populated[col] = frac
        if frac < 0.2:
            raise ValueError(
                f"family-B conditioning column {col} non-zero frac {frac:.3f}"
            )

    ss = (
        ssparams.load_ssa_parameters(pe_us_dir)
        if pe_us_dir
        else (ssparams.load_ssa_parameters())
    )
    weight = persons["weight"].to_numpy(dtype=np.float64)
    age = persons["age"].to_numpy(dtype=np.float64)
    fem = persons["is_female"].to_numpy(dtype=bool)
    birth_year = (REF_YEAR - age).astype(int)
    anchor_band = np.array(
        [(_di_anchor_band(a) or "") for a in age], dtype=object
    )

    di_band_labels = [b[0] for b in DI_ANCHOR_BANDS]
    # awards cohort: persons at claiming ages 62..FRA on the 2024 frame
    awards = (age >= 62) & (age <= FRA_AGE)

    di_draws = {lab: [] for lab in di_band_labels}
    conv_draws = {"female": [], "male": []}
    for k in range(k_draws):
        rng = np.random.default_rng(FAMILY_B_STREAM_BASE + k)
        on_di = simulate_di_status(persons, gens, rng)
        # di_prevalence: age composition of the DI stock, percentages
        stock = on_di
        stock_w = weight[stock]
        stock_band = anchor_band[stock]
        tot = stock_w.sum()
        for lab in di_band_labels:
            share = (
                100.0 * stock_w[stock_band == lab].sum() / tot
                if tot > 0
                else 0.0
            )
            di_draws[lab].append(share)
        # conversion: assign_claim_or_conversion over the awards cohort
        aw = awards
        people = pd.DataFrame(
            {
                "sex": np.where(fem[aw], "female", "male"),
                "birth_year": birth_year[aw],
                "on_di": on_di[aw],
            }
        )
        assigned = dconv.assign_claim_or_conversion(rng, people, 2022, ss)
        is_conv = assigned["claim_kind"].to_numpy() == "conversion"
        aw_w = weight[aw]
        for sex in ("female", "male"):
            m = people["sex"].to_numpy() == sex
            denom = aw_w[m].sum()
            share = (
                100.0 * aw_w[m & is_conv].sum() / denom if denom > 0 else 0.0
            )
            conv_draws[sex].append(share)

    gated = family_b_contract["gated_cells"]
    per_cell = {}
    all_pass = True
    for cell, spec in gated.items():
        if cell.startswith("di_prevalence."):
            lab = cell.split(".", 1)[1]
            draws = np.array(di_draws[lab])
        elif cell.startswith("claim_age.disability_conversion|"):
            sex = cell.split("|", 1)[1]
            draws = np.array(conv_draws[sex])
        else:
            continue
        deployed = float(np.mean(draws))
        anchor = float(spec["anchor_pp"])
        tol = float(spec["tolerance_pp"])
        dev = abs(deployed - anchor)
        cpass = dev <= tol
        all_pass = all_pass and cpass
        per_cell[cell] = {
            "deployed_pp": deployed,
            "anchor_pp": anchor,
            "abs_dev_pp": dev,
            "tolerance_pp": tol,
            "pass": bool(cpass),
            "across_draw_sd_pp": float(np.std(draws)),
        }

    # per-draw cube [K, gated_cell] for the dispersion disclosure
    cube_cells = list(per_cell)
    cube = []
    for cell in cube_cells:
        if cell.startswith("di_prevalence."):
            cube.append(di_draws[cell.split(".", 1)[1]])
        else:
            cube.append(conv_draws[cell.split("|", 1)[1]])
    cube = np.array(cube).T  # [K, cell]

    return {
        "pricing": "anchor (point values + reference-period vintage tolerance)",
        "populated_conditioning_columns": populated,
        "conditioning_columns": list(FAMILY_B_CONDITIONING_COLUMNS),
        "awards_cohort_n": int(awards.sum()),
        "cube_shape": list(cube.shape),
        "cube_cells": cube_cells,
        "cube": cube.tolist(),
        "per_cell": per_cell,
        "n_cells_pass": sum(1 for c in per_cell.values() if c["pass"]),
        "n_cells": len(per_cell),
        "family_b_pass": bool(all_pass),
    }


# --------------------------------------------------------------------------
# Family C -- the two ordinal compression fingerprints (BINARY, no floor).
# The committed #115 / #117 encodings re-run on the TRANSPORTED frame,
# changing ONLY the frame (the certified ledgers run verbatim; only the
# earnings source readers are swapped to the transported careers).
# --------------------------------------------------------------------------
#: Representative-earner window: CPS persons whose 2024 earnings represent
#: the cross-sectional earnings distribution (prime working age, positive).
FAMILY_C_EARNER_AGE_LO = 25
FAMILY_C_EARNER_AGE_HI = 60
#: The study's committed eligibility cohort window (born so age-62 falls in
#: 2005-2019); the transported careers are spread across it deterministically.
FAMILY_C_COHORT_LO = 1943
FAMILY_C_COHORT_HI = 1957
FAMILY_C_CAREER_AGES = tuple(
    range(22, 62, 2)
)  # biennial, matching PSID cadence
#: Permanent variance share of log earnings (issue #42 committed
#: autocorrelation-battery back-out: permanent 0.467 / transitory 0.364 /
#: noise 0.169) -- the persistence the gate-1 chain reproduces.
PERMANENT_VARIANCE_SHARE = 0.467
#: Transitory-mobility draw stream for family C (distinct from A=9100/B=9200).
FAMILY_C_TRANSITORY_STREAM = 9300


def transport_career_panel(
    persons: pd.DataFrame, gens: DeployedGenerators
) -> dict[str, pd.DataFrame]:
    """Transport CPS earners into career-earnings panels (study schema).

    For each representative earner (prime-age, positive 2024 earnings): the
    anchor rank ``u_A`` of their 2024 earnings in the certified gate-1 cell
    marginal, then a full ages-22-61 career whose LEVEL is anchored on the
    person's actual 2024 earnings and whose age-profile SHAPE is the gate-1
    marginal at ``u_A`` -- so the CPS earnings distribution (its heavy upper
    tail) flows into the transported AIME / taxable payroll. Birth years are
    spread deterministically across the committed cohort window so the
    certified ledgers' eligibility filter and indexing apply verbatim.
    """
    df = persons[
        (persons["age"] >= FAMILY_C_EARNER_AGE_LO)
        & (persons["age"] <= FAMILY_C_EARNER_AGE_HI)
        & (persons["earnings"] > 0)
    ].reset_index(drop=True)

    age0 = df["age"].to_numpy(dtype=np.float64)
    earn0 = df["earnings"].to_numpy(dtype=np.float64)
    fem = df["is_female"].to_numpy(dtype=bool)
    wt = df["weight"].to_numpy(dtype=np.float64)
    n = len(df)
    pid = (np.arange(n) + CPS_ID_OFFSET).astype(np.int64)

    marg = gens.earnings_marginals
    abin = gens.age_bin_fn
    bins0 = abin(age0)

    # anchor rank u_A per person: the persistent rank of their 2024 earnings
    # WITHIN their own CPS age band (the anchor_u convention -- rank within the
    # age cell -- computed on the CPS distribution, not the PSID marginal, so
    # the CPS-to-PSID nominal misalignment does not inflate ranks). This is
    # the k-NN chain's persistent-rank state, deployed on the CPS anchor.
    u_a = np.full(n, 0.5)
    have_rank = np.ones(n, dtype=bool)
    for b in np.unique(bins0):
        idx = np.nonzero(bins0 == b)[0]
        e = earn0[idx]
        # weighted within-band CDF rank (plotting position on frame weight)
        order = np.argsort(e, kind="stable")
        wsort = wt[idx][order]
        cum = np.cumsum(wsort) - 0.5 * wsort
        rank_sorted = cum / wsort.sum()
        r = np.empty(len(idx))
        r[order] = rank_sorted
        u_a[idx] = np.clip(r, 0.001, 0.999)

    # career earnings at each age = the certified gate-1 marginal quantile at
    # the person's per-year rank u_year, where u_year carries a PERSISTENT
    # component (the anchor rank u_A) plus TRANSITORY mobility -- the certified
    # earnings process's structure, deployed. In latent-normal space
    # z_year = sqrt(rho)*z_A + sqrt(1-rho)*eps, with the permanent variance
    # share rho pinned to the committed autocorrelation battery back-out
    # (issue #42: permanent share 0.467) the gate-1 chain reproduces. Fixing
    # the rank across the career (rho=1) removes mobility and over-concentrates
    # the tail; the permanent+transitory structure restores realistic career
    # earnings dispersion. Levels remain gate-1-generated (never the frame's
    # own history, which it does not carry). Transitory draws use a fixed
    # dedicated stream (default_rng(9300)) so family C is reproducible.
    from scipy.stats import norm

    z_a = norm.ppf(np.clip(u_a, 1e-4, 1 - 1e-4))
    rho = PERMANENT_VARIANCE_SHARE
    trng = np.random.default_rng(FAMILY_C_TRANSITORY_STREAM)
    career_bins = {
        a: int(abin(np.array([float(a)]))[0]) for a in FAMILY_C_CAREER_AGES
    }

    rows = []
    span = FAMILY_C_COHORT_HI - FAMILY_C_COHORT_LO + 1
    birth_year = FAMILY_C_COHORT_LO + (np.arange(n) % span)
    for a in FAMILY_C_CAREER_AGES:
        cell = marg.get((career_bins[a], TERMINAL_PERIOD))
        if cell is None or cell.n_pos == 0:
            continue
        eps = trng.standard_normal(n)
        z_year = np.sqrt(rho) * z_a + np.sqrt(1.0 - rho) * eps
        u_year = np.clip(norm.cdf(z_year), 0.001, 0.999)
        earn_year = cell.quantile(u_year)
        yr = birth_year + a
        for i in range(n):
            e = earn_year[i]
            if e > 0:
                rows.append(
                    (int(pid[i]), int(yr[i]), float(e), int(a), float(wt[i]))
                )
    good = have_rank
    panel = pd.DataFrame(
        rows, columns=["person_id", "period", "earnings", "age", "weight"]
    )
    panel["role"] = "head"
    panel["earnings_acc"] = 0

    # Diagnostic: fraction of transported taxable payroll above the 2024 wage
    # base (the C2 break-even is ~16.1%). Disclosed with the caveat that the
    # transported careers -- positive earners only, no zero/low-earning years,
    # permanent+transitory rank structure -- over-concentrate the upper tail
    # relative to the true representative frame, so this fraction is an UPPER
    # read on the transport's tail (which makes the non-reversal conservative).
    _e = panel["earnings"].to_numpy(dtype=np.float64)
    _wagebase_2024 = 168600.0
    frac_payroll_above = (
        float(_e[_e > _wagebase_2024].sum() / _e.sum()) if _e.sum() else 0.0
    )

    sex_df = pd.DataFrame(
        {
            "person_id": pid[good],
            "sex": np.where(fem[good], "female", "male"),
        }
    )
    # Empty birth_history (full schema): no simulated children, so the
    # caregiver credits are zero -- the caregiver plans are report-only in
    # C1 (only PI/PPI/NRA/COLA gate); the caregiver study still forms the
    # same career set via the coverage filter.
    births = pd.DataFrame(
        {
            "parent_person_id": pd.array([], dtype="Int64"),
            "parent_sex": pd.array([], dtype="string"),
            "parent_birth_year": pd.array([], dtype="Int64"),
            "parent_birth_month": pd.array([], dtype="Int64"),
            "record_type": pd.array([], dtype="string"),
            "child_person_id": pd.array([], dtype="Int64"),
            "child_sex": pd.array([], dtype="string"),
            "birth_year": pd.array([], dtype="Int64"),
            "birth_month": pd.array([], dtype="Int64"),
            "birth_order": pd.array([], dtype="Int64"),
        }
    )
    return {
        "panel": panel,
        "sex": sex_df,
        "births": births,
        "n_earners": int(good.sum()),
        "n_person_years": int(len(panel)),
        "frac_payroll_above_wage_base": frac_payroll_above,
        "permanent_variance_share": PERMANENT_VARIANCE_SHARE,
    }


def _kendall_tau(order_a: list[str], order_b: list[str]) -> float:
    """Kendall tau between two orderings of the same items."""
    rank_b = {v: i for i, v in enumerate(order_b)}
    seq = [rank_b[v] for v in order_a]
    n = len(seq)
    con = dis = 0
    for i in range(n):
        for j in range(i + 1, n):
            if seq[i] < seq[j]:
                con += 1
            else:
                dis += 1
    total = con + dis
    return (con - dis) / total if total else 1.0


def family_c(
    persons: pd.DataFrame, gens: DeployedGenerators, family_c_contract: dict
) -> dict[str, Any]:
    """Re-run both compression fingerprints on the transported frame.

    Monkeypatches ONLY the earnings-source readers the certified #115/#117
    studies call (family_earnings_panel, marriage_history, birth_history) to
    return the transported careers, then runs the committed ledgers verbatim
    and compares the resulting orderings to the required representative
    orderings (Kendall tau 1.0 vs the anchor).
    """
    import importlib
    import sys
    from pathlib import Path

    scripts = Path(__file__).resolve().parents[3] / "scripts"
    if str(scripts) not in sys.path:
        sys.path.insert(0, str(scripts))
    mr = importlib.import_module("replication_mermin_rows")
    cg = importlib.import_module("replication_caregiver")
    m2 = importlib.import_module("m2_pseudo_projection")
    co = importlib.import_module("replication_cost_ordering")

    transported = transport_career_panel(persons, gens)
    panel, sex_df, births = (
        transported["panel"],
        transported["sex"],
        transported["births"],
    )

    def fake_panel(*a, **k):
        return panel.copy()

    def fake_marriage(*a, **k):
        return sex_df.copy()

    def fake_births(*a, **k):
        return births.copy()

    patched = []
    for mod in (mr, cg, m2, co):
        for name, fn in (
            ("family_earnings_panel", fake_panel),
            ("marriage_history", fake_marriage),
            ("birth_history", fake_births),
        ):
            if hasattr(mod, name):
                patched.append((mod, name, getattr(mod, name)))
                setattr(mod, name, fn)
    try:
        m2_res = m2.run(verbose=False)
    finally:
        for mod, name, orig in patched:
            setattr(mod, name, orig)

    rvf = m2_res.get("results_vs_forecasts", {})
    detail = m2_res.get("forecasts_detail", {})
    c1_order = list(rvf["F4"]["result_order"])
    c2_order = list(rvf["F2"]["result_order"])

    out = {}
    for fid, order, fkey in (("c1", c1_order, "F4"), ("c2", c2_order, "F2")):
        spec = family_c_contract["fingerprints"][fid]
        required = list(spec["required_representative_order"])
        psid = list(spec["psid_frame_order"])
        out[fid] = {
            "id": spec["id"],
            "name": spec["name"],
            "psid_frame_order": psid,
            "required_representative_order": required,
            "deployed_order": order,
            "kendall_tau_vs_required": _kendall_tau(order, required),
            "kendall_tau_vs_psid": _kendall_tau(order, psid),
            "reversed_to_anchor": bool(order == required),
            "required_swap_pair": spec.get("swap_pair"),
            "required_swap_realised": _swap_realised(
                order, spec.get("swap_pair")
            ),
            "provision_deltas": _provision_values(
                rvf.get(fkey, {}), detail.get(fkey, {})
            ),
        }

    return {
        "n_earners_transported": transported["n_earners"],
        "n_person_years": transported["n_person_years"],
        "frac_payroll_above_wage_base": transported[
            "frac_payroll_above_wage_base"
        ],
        "permanent_variance_share": transported["permanent_variance_share"],
        "transport_tail_caveat": (
            "Transported careers are positive prime-age earners with no "
            "zero/low-earning years and a permanent+transitory rank structure, "
            "which over-concentrates the upper tail vs the true representative "
            "frame (frac payroll above wage base is an UPPER read). A heavier "
            "tail FAVOURS reversal, so the non-reversal below is conservative."
        ),
        "fingerprints": out,
        "both_reverse": bool(
            out["c1"]["reversed_to_anchor"] and out["c2"]["reversed_to_anchor"]
        ),
        "family_c_pass": bool(
            out["c1"]["reversed_to_anchor"] and out["c2"]["reversed_to_anchor"]
        ),
    }


def _swap_realised(order: list[str], swap_pair) -> bool | None:
    """Whether the required adjacent swap happened (the swapped item now
    outranks the other), regardless of other adjacency changes."""
    if not swap_pair or len(swap_pair) != 2:
        return None
    a, b = swap_pair  # required: a should now be BELOW b (b outranks a)
    if a not in order or b not in order:
        return None
    return order.index(b) < order.index(a)


def _provision_values(rvf_block: dict, detail_block: dict) -> dict:
    """Pull per-provision delta values for the report (best-effort)."""
    vals = {}
    for k in ("result_order", "kendall_tau", "kendall_tau_vs_smith", "met"):
        if k in rvf_block:
            vals[k] = rvf_block[k]
    for k, v in detail_block.items():
        if isinstance(v, (int, float, dict, list)):
            vals[k] = v
    return vals
